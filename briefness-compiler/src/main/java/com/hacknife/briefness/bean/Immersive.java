package com.hacknife.briefness.bean;

public class Immersive {
    String statusColor;
    String navigationColor;
    String statusEmbed;
    String navigationEmbed;

    public Immersive() {
    }

    public String getStatusColor() {
        return statusColor;
    }

    public void setStatusColor(String statusColor) {
        this.statusColor = statusColor;
    }

    public String getNavigationColor() {
        return navigationColor;
    }

    public void setNavigationColor(String navigationColor) {
        this.navigationColor = navigationColor;
    }

    public String getStatusEmbed() {
        return statusEmbed;
    }

    public void setStatusEmbed(String statusEmbed) {
        this.statusEmbed = statusEmbed;
    }

    public String getNavigationEmbed() {
        return navigationEmbed;
    }

    public void setNavigationEmbed(String navigationEmbed) {
        this.navigationEmbed = navigationEmbed;
    }
}
